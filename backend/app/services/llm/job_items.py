import os

from langchain_core.prompts import ChatPromptTemplate
from langchain_ollama import ChatOllama

from .general import GeneralLLMClient


class CoverLetterPrompt(GeneralLLMClient):

    def __init__(self):
        base_url = os.getenv("OLLAMA_HOST", "http://localhost:11434")
        model = ChatOllama(model="mistral:7b", temperature=0.3, base_url=base_url)
        super().__init__(model=model)

    def get_schema(self):
        return None
    
    @property
    def prompt_template(self):
        system_message = """
        Ты — профессиональный карьерный консультант и копирайтер с опытом написания сопроводительных писем для IT-специалистов. Твоя задача — создавать персонализированные сопроводительные письма, которые проходят ATS-фильтры и цепляют HR.

        СТРОГИЕ ПРАВИЛА:
        1. Пиши ТОЛЬКО на {lang} языке или на русском языке.
        2. Письмо должно быть от первого лица (от имени кандидата).
        3. Длина письма: 180-280 слов. Не больше, не меньше.
        4. Структура письма:
        - Приветствие (Добрый день) без обращений
        - Релевантный опыт: упомяни 2-3 самых подходящих проекта из user_projects, ставя САМЫЙ РЕЛЕВАНТНЫЙ к требованиям вакансии ПЕРВЫМ
        - Конкретные достижения с цифрами/результатами (используй achievements, адаптируй формулировки под вакансию)
        - Технологический стек: естественно вплети ключевые слова из requirements

        КАК РАБОТАТЬ С КЛЮЧЕВЫМИ СЛОВАМИ (ATS):
        - Извлеки из requirements все технологии, инструменты и навыки.
        - Используй ТОЧНЫЕ формулировки из requirements (например, если в вакансии написано "PostgreSQL", не пиши "Postgres").
        - Включи минимум 70% ключевых слов из requirements, но органично, без списков и перечислений через запятую длиннее 4-5 элементов.

        КАК ПОДАВАТЬ ПРОЕКТЫ:
        - Самый первый проект описывай подробнее (2-3 предложения), остальные — кратко (1 предложение).
        - Достижения (achievements) переформулируй так, чтобы они отражали именно те качества, которые ищет работодатель в вакансии {name}.

        ЧЕГО ИЗБЕГАТЬ:
        - Избегай разных языков в письме
        - Не выдумывай проекты, технологии или достижения, которых нет в user_projects.
        - Не используй клише вроде "командный игрок", "стрессоустойчивый", "быстро обучаюсь" без подкрепления фактами.
        - Не пиши маркированные списки и заголовки — только связный текст абзацами.
        - Не начинай с "Здравствуйте, меня зовут..." — начни с конкретного интереса к позиции.
        - Не используй эмодзи.

        ПОДПИСЬ:
        - В конце письма поставь подпись «С уважением, {user_first_name} {user_last_name}», если имя и фамилия указаны.
        - Если оба значения пусты — закончи нейтральной фразой готовности к интервью без подписи.

        ФОРМАТ ВЫВОДА:
        Верни ТОЛЬКО текст сопроводительного письма, без преамбулы вроде "Вот ваше письмо:" и без пояснений после."""


        human_message = """Сгенерируй сопроводительное письмо на вакансию по следующим данным:

        ИМЯ И ФАМИЛИЯ КАНДИДАТА: {user_first_name} {user_last_name}

        НАЗВАНИЕ ВАКАНСИИ:
        {name}

        НАЗВАНИЕ ПРОЕКТА/КОМПАНИИ РАБОТОДАТЕЛЯ:
        {project_name}

        ТРЕБОВАНИЯ ВАКАНСИИ И КЛЮЧЕВЫЕ СЛОВА:
        {requirements}

        МОИ ПРОЕКТЫ (JSON):
        {user_projects}

        ИНСТРУКЦИЯ ПО ВЫПОЛНЕНИЮ:
        - Проанализируй requirements и выдели список ключевых технологий и навыков.
        - Отсортируй проекты по убыванию релевантности.
        - Напиши письмо, начиная с названия проекта-работодателя ({project_name}) и должность ({name}) в первом абзаце.
        - Переформулируй мои achievements под язык вакансии — подчеркни те качества и результаты, которые важны для {name}.
        - Естественно вплети ключевые слова из requirements в текст.
        - Заверши письмо выражением готовности обсудить детали на интервью.

        Напиши письмо сейчас."""
        
        return ChatPromptTemplate.from_messages([system_message,human_message])